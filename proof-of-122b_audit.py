def run_audit() -> dict:
    """Run the entropy audit and return results as a dictionary.
    
    Returns:
        Dictionary containing all audit results and metadata
    """
    # Initialize results
    results = {
        'timestamp': time.strftime('%Y-%m-%d %H:%M:%S UTC', time.gmtime()),
        'audit_tool': 'SMI Password Entropy Audit Tool',
        'audit_version': '1.0.0',
        'security_claim': f'All passwords have at least {MIN_ENTROPY_BITS} bits of entropy',
        'configuration': {
            'max_iterations': MAX_ITERATIONS,
            'min_entropy_bits': MIN_ENTROPY_BITS,
            'confidence_sigma': CONFIDENCE_SIGMA,
            'key_length_bytes': len_bytes,
            'password_length_chars': min_password_length,
            'base95_alphabet_size': len(BASE95_ALPHABET),
            'hash_algorithm': 'SHA-256',
        },
        'keys': {
            'ikm_hex': IKM_HEX,
            'salt_hex': SALT_HEX,
        },
        'statistics': {},
        'verdict': None,
    }
    
    # Derive PRK
    ikm = bytes.fromhex(IKM_HEX)
    salt = bytes.fromhex(SALT_HEX)
    info_prefix = f"okm:v1:{len_bytes}:proof of entropy"
    
    prk = hkdf_extract(salt, ikm, hash=hashlib.sha256)
    results['keys']['prk_hex'] = prk.hex()
    
    # Clear IKM from memory (best effort)
    del ikm
    
    # Initialize Welford's online algorithm
    n = 0
    mean = 0.0
    M2 = 0.0
    min_entropy = float('inf')
    max_entropy = float('-inf')
    interrupted = False
    failures = 0
    
    # Signal handler for graceful Ctrl+C
    def signal_handler(sig, frame):
        nonlocal interrupted
        interrupted = True
        print("\n[!] Ctrl+C caught, completing current test...", file=sys.stderr)
    
    signal.signal(signal.SIGINT, signal_handler)
    
    start_time = time.time()
    
    # Test loop
    print(f"Starting audit: Testing {MAX_ITERATIONS:,} passwords...")
    print(f"Target: >= {MIN_ENTROPY_BITS} bits of entropy per password")
    
    while n < MAX_ITERATIONS and not interrupted:
        # Generate password
        info = f"{info_prefix}{n}".encode('utf-8')
        pw = generate_password(prk, info, len_bytes)
        
        # Calculate entropy
        entropy = shannon_entropy(pw)
        
        # Update statistics
        if entropy < min_entropy:
            min_entropy = entropy
        if entropy > max_entropy:
            max_entropy = entropy
        
        # Welford's algorithm for online mean/variance
        n += 1
        delta = entropy - mean
        mean += delta / n
        delta2 = entropy - mean
        M2 += delta * delta2
        
        # Check if password meets requirement
        if entropy < MIN_ENTROPY_BITS:
            failures += 1
            print(f"  [FAIL] Password {n}: {entropy:.2f} bits < {MIN_ENTROPY_BITS}")
            results['failures'] = results.get('failures', []) + [{
                'iteration': n,
                'password': pw,
                'entropy_bits': entropy,
            }]
        
        # Progress update (every 100k iterations)
        if n % 100_000 == 0:
            current_stddev = math.sqrt(M2 / n) if n > 0 else 0
            elapsed = time.time() - start_time
            rate = n / elapsed if elapsed > 0 else 0
            print(f"  Progress: {n:,}/{MAX_ITERATIONS:,} "
                  f"({100*n/MAX_ITERATIONS:.1f}%) "
                  f"| Avg: {mean:.2f} ± {current_stddev:.2f} bits "
                  f"| Rate: {rate:,.0f} passwords/sec")
    
    # Final calculations
    elapsed = time.time() - start_time
    rate = n / elapsed if elapsed > 0 else 0
    variance = M2 / n if n > 0 else 0
    stddev = math.sqrt(variance)
    
    # Store statistics
    results['statistics'] = {
        'total_tested': n,
        'total_failed': failures,
        'completion_percentage': 100 * n / MAX_ITERATIONS,
        'average_entropy_bits': mean,
        'variance_bits_squared': variance,
        'std_deviation_bits': stddev,
        'min_entropy_bits': min_entropy,
        'max_entropy_bits': max_entropy,
        'elapsed_seconds': elapsed,
        'rate_passwords_per_second': rate,
    }
    
    # Determine verdict
    if failures > 0:
        results['verdict'] = 'FAILED'
        results['verdict_message'] = f"{failures} out of {n} passwords failed the entropy test"
    elif min_entropy < MIN_ENTROPY_BITS:
        results['verdict'] = 'FAILED'
        results['verdict_message'] = f"Minimum entropy ({min_entropy:.2f} bits) is below {MIN_ENTROPY_BITS}"
    elif interrupted:
        results['verdict'] = 'INCOMPLETE'
        results['verdict_message'] = f"Audit interrupted after {n:,} tests"
    else:
        results['verdict'] = 'PASSED'
        confidence_bound = CONFIDENCE_SIGMA * stddev
        results['verdict_message'] = (
            f"All {n:,} passwords tested have >= {MIN_ENTROPY_BITS} bits of entropy. "
            f"With {CONFIDENCE_SIGMA}-sigma confidence ({100*(1 - 2*(1-0.5*math.erfc(CONFIDENCE_SIGMA/math.sqrt(2)))):.4f}% confidence level), "
            f"the true minimum is >= {min_entropy - confidence_bound:.2f} bits."
        )
    
    return results


