def base95_encode(data: bytes, min_length: int = None) -> str:
    """Encode bytes to base95 string using ASCII printable characters.
    
    This encoding preserves all entropy from the input bytes by padding to
    the minimum length required to represent the full bit space.
    
    Args:
        data: Bytes to encode
        min_length: Minimum output length (for entropy preservation)
        
    Returns:
        Base95-encoded string
    """
    if len(data) == 0:
        return ''
    
    alphabet_len = len(BASE95_ALPHABET)
    num = int.from_bytes(data, byteorder='big')
    
    result = []
    
    # Handle all zeros case - need to preserve entropy
    if num == 0:
        if min_length is None:
            min_length = math.ceil(len(data) * 8 / math.log2(alphabet_len))
        return BASE95_ALPHABET[0] * min_length
    
    # Encode using division-modulo algorithm
    while num > 0:
        num, remainder = divmod(num, alphabet_len)
        result.append(BASE95_ALPHABET[remainder])
    
    encoded = ''.join(reversed(result))
    
    # Pad to minimum length if specified
    if min_length is not None and len(encoded) < min_length:
        padding = BASE95_ALPHABET[0] * (min_length - len(encoded))
        encoded = padding + encoded
    
    return encoded


def shannon_entropy(data: str) -> float:
    """Calculate Shannon entropy of a string in bits.
    
    Shannon entropy measures the average information content per symbol.
    For a string of length L with character probabilities p_i:
        H = -Σ p_i * log2(p_i) * L
    
    This returns the TOTAL entropy in bits, not bits per symbol.
    
    Args:
        data: String to analyze
        
    Returns:
        Shannon entropy in bits
    """
    if not data:
        return 0.0
    
    # Count frequency of each character
    freq = {}
    for char in data:
        freq[char] = freq.get(char, 0) + 1
    
    length = len(data)
    entropy = 0.0
    
    # Calculate Shannon entropy: -Σ p(x) * log2(p(x))
    for count in freq.values():
        p = count / length
        if p > 0:
            entropy -= p * math.log2(p)
    
    # Return total entropy in bits (not bits per symbol)
    return entropy * length


def generate_password(prk: bytes, info: bytes, length: int) -> str:
    """Generate a password using HKDF and base95 encoding.
    
    This replicates the production password generation algorithm.
    
    Args:
        prk: Pseudo-random key from HKDF extract phase
        info: Context/info string for HKDF expand phase
        length: Number of bytes to generate
        
    Returns:
        Generated password string
    """
    # HKDF expand phase
    okm = hkdf_expand(prk, info, length=length, hash=hashlib.sha256)
    
    # Encode to base95
    return base95_encode(okm, min_length=min_password_length)


